<script>
    const socket = new WebSocket('wss://ws-feed.pro.coinbase.com');
    let coinData = {};

    function formatNumber(number) {
        if (number >= 1e9) {
            return (number / 1e9).toFixed(3) + 'B';
        } else if (number >= 1e6) {
            return (number / 1e6).toFixed(3) + 'M';
        } else if (number >= 1e3) {
            return (number / 1e3).toFixed(3) + 'K';
        } else {
            return number.toFixed(3);
        }
    }

    function updateCoinData(data, coin) {
        const currentPrice = parseFloat(data.price);

        if (!coinData[data.product_id]) {
            coinData[data.product_id] = {
                previousPrice: parseFloat(data.open_24h),
                percentChange: 0
            };
        }

        const previousPrice = coinData[data.product_id].previousPrice;
        const percentChange = ((currentPrice - previousPrice) / previousPrice) * 100;

        coinData[data.product_id].previousPrice = currentPrice;
        coinData[data.product_id].percentChange = percentChange;

        const formattedVolume = formatNumber(parseFloat(data.volume_30d));
        const productIdParts = data.product_id.split('-');

        const symbol = productIdParts[0];

        const newRow = `<tr class="table-coin" data-product-id="${data.product_id}">
                             <td class="table-coin-left">
                                <img src="images/icons/${coin.icon}" alt="${symbol}">
                                <p>${coin.full_name} <span>${symbol}</span></p>
                             </td>
                             <td class="price">$ ${formatNumber(currentPrice)}</td>
                             <td class="percent">${percentChange !== 0 ? (percentChange >= 0 ? '' : '') + percentChange.toFixed(3).replace(/-/, '') : '0.000'}%</td>
                             <td class="volume">${formattedVolume}</td>
                             <td><button class="button-trade">Trade</button></td>
                          </tr>`;

        const $newRow = $(newRow);
        const $existingRow = $(`#coin_table tr[data-product-id="${data.product_id}"]`);

        if ($existingRow.length > 0) {
            const $percent = $existingRow.find('.percent');
            const $price = $existingRow.find('.price');
            const $volume = $existingRow.find('.volume');

            $price.text(`$ ${formatNumber(currentPrice)}`);
            $percent.text(`${percentChange !== 0 ? (percentChange >= 0 ? '' : '') + percentChange.toFixed(3).replace(/-/, '') : '0.000'}%`);
            $volume.text(`${formattedVolume}`);

            $percent.removeClass('green-text red-text');

            if (percentChange > 0) {
                $percent.addClass('green-text');
            } else if (percentChange < 0) {
                $percent.addClass('red-text');
            }
        } else {
            $('#coin_table').append($newRow);

            if (percentChange > 0) {
                $newRow.find('.percent').addClass('green-text');
            } else if (percentChange < 0) {
                $newRow.find('.percent').addClass('red-text');
            }
        }
    }

    $(document).ready(function() {
      let coins;
      const coinsWebSocket = new WebSocket('wss://ws-feed.pro.coinbase.com');

      coinsWebSocket.onopen = function () {
        $.ajax({
            url: 'admin/functions/get_coins.php',
            type: 'GET',
            dataType: 'json',
            success: function (response) {
                coins = response;
                coins.forEach(function (coin) {
                    if (coinsWebSocket.readyState === coinsWebSocket.OPEN) {
                        coinsWebSocket.send(JSON.stringify({
                            type: 'subscribe',
                            channels: [{ name: 'ticker', product_ids: [coin.short_name + '-USD'] }]
                        }));
                    }
                });
            }
        });
    };

    coinsWebSocket.onmessage = function (event) {
        const data = JSON.parse(event.data);

        if (data.product_id && data.price && data.volume_30d !== undefined) {
            if (Array.isArray(coins)) {
                const coin = coins.find(coin => `${coin.short_name}-USD` === data.product_id);
                if (coin) {
                    updateCoinData(data, coin);
                }
            }
        }
    };

    });
